}
+ (UIColor *) coolGreen {
    return [UIColor colorWithRed:0.239 green:0.816 blue:0.729 alpha:1.0];
}
+ (UIColor *) coolYellow {
    return [UIColor colorWithRed:0.984 green:0.694 blue:0.137 alpha:1.0];
}
+ (UIColor *) coolRed {
    return [UIColor colorWithRed:0.973 green:0.286 blue:0.302 alpha:1.0];
}
+ (UIColor *) coolGray {
    return [UIColor colorWithRed:0.906 green:0.906 blue:0.906 alpha:1.0];
}
